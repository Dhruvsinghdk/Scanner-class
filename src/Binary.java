import java.util.Scanner;

public class Binary {
    public static void main(String[] args) {
        Scanner n = new Scanner(System.in);
        int x = n.nextInt(16);
        int y = n.nextInt(8);
        int z = n.nextInt(2);
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
    }
}
